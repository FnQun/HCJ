	function add1(i){
  console.log("函数声明："+(i+1));
}
add1(1);
 
var add1 = function(i){
  console.log("函数表达式："+(i+10));
}
add1(1);
 
function add1(i) {
    console.log("函数声明："+(i+100));
}
add1(1);
//没有重载,声明提前同名函数后面的覆盖前面的,函数声明会提前.//
//一般格式先声明再调用//
//函数声明自动提前,调用可以写在前面//
//函数表达式不会自动提前,调用必须写在后面.(否则在严格模式相爱会报错)//
//以上代码输出:
// 函数声明：101
// 函数表达式：11
// 函数表达式：11
